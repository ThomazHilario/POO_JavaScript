/*
    -> Math
    - Objeto com métodos para resoluções matemáticas
    - math.Floor() -> arredonda um número para baixo
    - Math.round() -> arredonda para um número mais proximo
    - Math.ceil() -> arredonda para um número acima
    - Math.random() -> Gera números aleatórios
*/

// Usando o Math.random() para gerar número ate um certo valor

console.log(Math.floor(Math.random() * 11))

// Usando o Math.floor()

console.log(Math.floor(14.55))

// Usando o Math.round()

console.log(Math.round(13.35))

// Usando o Math.ceil

console.log(Math.ceil(14.50))